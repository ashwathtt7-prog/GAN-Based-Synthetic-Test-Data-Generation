CREATE TABLE DEMO_CUSTOMER (
    DEMO_CUSTOMER_ID INTEGER PRIMARY KEY,
    CUSTOMER_NAME TEXT NOT NULL,
    CUSTOMER_EMAIL TEXT,
    CUSTOMER_SEGMENT TEXT NOT NULL,
    CUSTOMER_CITY TEXT NOT NULL,
    CUSTOMER_CREATED_AT TEXT NOT NULL
);

CREATE TABLE DEMO_PRODUCT (
    DEMO_PRODUCT_ID INTEGER PRIMARY KEY,
    PRODUCT_NAME TEXT NOT NULL,
    PRODUCT_CATEGORY TEXT NOT NULL,
    UNIT_PRICE REAL NOT NULL,
    IS_ACTIVE TEXT NOT NULL,
    PRODUCT_CREATED_AT TEXT NOT NULL
);

CREATE TABLE DEMO_ORDER (
    DEMO_ORDER_ID INTEGER PRIMARY KEY,
    DEMO_CUSTOMER_ID INTEGER NOT NULL,
    ORDER_NUMBER TEXT NOT NULL,
    ORDER_STATUS TEXT NOT NULL,
    ORDER_DATE TEXT NOT NULL,
    SHIP_DATE TEXT,
    ORDER_TOTAL REAL NOT NULL,
    SALES_CHANNEL TEXT NOT NULL,
    FOREIGN KEY (DEMO_CUSTOMER_ID) REFERENCES DEMO_CUSTOMER (DEMO_CUSTOMER_ID)
);

CREATE TABLE DEMO_ORDER_ITEM (
    DEMO_ORDER_ITEM_ID INTEGER PRIMARY KEY,
    DEMO_ORDER_ID INTEGER NOT NULL,
    DEMO_PRODUCT_ID INTEGER NOT NULL,
    QUANTITY INTEGER NOT NULL,
    UNIT_PRICE REAL NOT NULL,
    LINE_TOTAL REAL NOT NULL,
    DISCOUNT_PCT REAL NOT NULL,
    FOREIGN KEY (DEMO_ORDER_ID) REFERENCES DEMO_ORDER (DEMO_ORDER_ID),
    FOREIGN KEY (DEMO_PRODUCT_ID) REFERENCES DEMO_PRODUCT (DEMO_PRODUCT_ID)
);
